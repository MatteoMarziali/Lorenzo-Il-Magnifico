package it.polimi.ingsw.ps19;

public class Bonus {
	
	int harvestModification; //it means the increase/decrease of the Harvest action value for the specific player
	int productionModification; // "  "  "  " 
	
	int takingBuildingCardBonus;
	int takingCharacterCardBonus;
	int takingVentureCardBonus;
	int takingTerritoryBonus;
	public int getHarvestModification() {
		return harvestModification;
	}
	public void setHarvestModification(int harvestModification) {
		this.harvestModification = harvestModification;
	}
	public int getProductionModification() {
		return productionModification;
	}
	public void setProductionModification(int productionModification) {
		this.productionModification = productionModification;
	}
	public int getTakingBuildingCardBonus() {
		return takingBuildingCardBonus;
	}
	public void setTakingBuildingCardBonus(int takingBuildingCardBonus) {
		this.takingBuildingCardBonus = takingBuildingCardBonus;
	}
	public int getTakingCharacterCardBonus() {
		return takingCharacterCardBonus;
	}
	public void setTakingCharacterCardBonus(int takingCharacterCardBonus) {
		this.takingCharacterCardBonus = takingCharacterCardBonus;
	}
	public int getTakingVentureCardBonus() {
		return takingVentureCardBonus;
	}
	public void setTakingVentureCardBonus(int takingVentureCardBonus) {
		this.takingVentureCardBonus = takingVentureCardBonus;
	}
	public int getTakingTerritoryBonus() {
		return takingTerritoryBonus;
	}
	public void setTakingTerritoryBonus(int takingTerritoryBonus) {
		this.takingTerritoryBonus = takingTerritoryBonus;
	}
	
	

}
